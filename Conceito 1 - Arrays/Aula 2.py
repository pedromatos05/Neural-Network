import numpy as np

# Operações Basicas com Arrays

a = np.array([1, 2, 3])
b = np.array([4, 5, 6])

print(a + b) # Isto vai somar os elementos correspondentes dos arrays a e b
print(a * b) # Isto vai multiplicar os elementos correspondentes dos arrays a e b
print (a * 2) # Isto vai multiplicar todos os elementos do array a por 2
print(a ** 2) # Muito usado para calcular erros  