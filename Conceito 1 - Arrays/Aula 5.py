import numpy as np 

dados = np.array([2, 4, 6, 8, 10])

print(np.mean(dados)) # Diz qual é a média 
print(np.sum(dados)) # Soma os dados todos
print(np.max(dados)) # Diz qual é máximo nos dados
print(np.min(dados)) # Diz qual é o mínimo nos dados
print(np.std(dados)) # O desvio padrao (dispersão de dados)